Point::Point( const Point& other ) :
	_x(other._x),
	_y(other._y)
{}

/* ******************************* destructor ****************************** */

Point::~Point()
{}

/* ********************** copy assignment operator ************************** */

Point& Point::operator=( const Point& other )
{
	// non assignable due to const variables
	(void)other;
	return (*this);
}

