/* ******************************* constructors ***************************** */

HumanB::HumanB( const std::string& name ) :
	_name(name)
{}

/* ******************************* destructors ***************************** */

HumanB::~HumanB()
{}

/* ******************************** getters ******************************** */

/* ******************************** setters ******************************** */

void	HumanB::setWeapon( Weapon& weapon )
{
	_weapon = &weapon;
}

/* ************************* other public methods ************************** */

void	HumanB::attack()
{
	if (!_weapon)
		std::cout << _name << " has no weapon" << std::endl;
	else
		std::cout << _name << " attacks with their " << _weapon->getType() << std::endl;
}

