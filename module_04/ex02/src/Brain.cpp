/* *************************** default constructor ************************** */

Brain::Brain()
{
	for (int i = 0; i < 100; i++)
	{
		std::stringstream	ss;
		ss << "idea " << i;
		_ideas[i] = ss.str();
	}
}

/* ******************************* destructor ****************************** */

Brain::~Brain()
{
	std::cout << "Brain destructor." << std::endl;
}

/* ***************************** copy constructor *************************** */

Brain::Brain( const Brain& other )
{
	for (int i = 0; i < 100; i++)
		_ideas[i] = other._ideas[i]; 
}

/* ********************** copy assignment operator ************************** */

Brain& Brain::operator=( const Brain& other )
{
	if (this != &other)
	{
		for (int i = 0; i < 100; i++)
			_ideas[i] = other._ideas[i];
	}
	return (*this);
}

