/* *************************** default constructor ************************** */

AMateria::AMateria() :
	_type("none")
{}

/* ******************************* destructor ****************************** */

AMateria::~AMateria()
{}

/* ***************************** copy constructor *************************** */

AMateria::AMateria( const AMateria& other ) :
	_type(other._type)
{}

/* ********************** copy assignment operator ************************** */

AMateria& AMateria::operator=( const AMateria& other )
{
	if (this != &other)
		_type = other.getType();
	return (*this);
}

/* ************************************************************************** */
/*								public methods  							  */
/* ************************************************************************** */

/* ******************************* constructors ***************************** */

AMateria::AMateria( std::string const & type ) :
	_type(type)
{}

/* ******************************** getters ********************************* */

std::string	const &	AMateria::getType() const
{
	return (_type);
}

